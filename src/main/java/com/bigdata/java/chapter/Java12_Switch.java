package com.bigdata.java.chapter;

public class Java12_Switch {
    public static void main(String[] args) {
        int i = 1;
        switch (i) {
            default:
                System.out.println("dddddd");
                break;
            case 0:
                break;
            case 1:
                System.out.println("111111");
        }

    }
}
